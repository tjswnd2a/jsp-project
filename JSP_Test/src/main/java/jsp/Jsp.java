package jsp;

public class Jsp {
	private int jspID;
	private String jspTitle;
	private String jspDate;
	private String userID;
	private String jspContent;
	private int jspAvailable;
	
	
	public int getJspID() {
		return jspID;
	}
	public void setJspID(int jspID) {
		this.jspID = jspID;
	}
	public String getJspTitle() {
		return jspTitle;
	}
	public void setJspTitle(String jspTitle) {
		this.jspTitle = jspTitle;
	}
	public String getJspDate() {
		return jspDate;
	}
	public void setJspDate(String jspDate) {
		this.jspDate = jspDate;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getJspContent() {
		return jspContent;
	}
	public void setJspContent(String jspContent) {
		this.jspContent = jspContent;
	}
	public int getJspAvailable() {
		return jspAvailable;
	}
	public void setJspAvailable(int jspAvailable) {
		this.jspAvailable = jspAvailable;
	}
	
	
}
